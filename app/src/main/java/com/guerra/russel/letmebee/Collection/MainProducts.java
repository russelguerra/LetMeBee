package com.guerra.russel.letmebee.Collection;

public class MainProducts {

    private String name;
    private String image;

    public MainProducts() {
    }

    public MainProducts(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }
}
